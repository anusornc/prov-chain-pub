#!/usr/bin/env python3
"""Generate deterministic supply-chain benchmark datasets for scale-up campaigns."""

from __future__ import annotations

import argparse
import hashlib
import json
import math
from datetime import datetime, timedelta, timezone
from pathlib import Path


PRODUCT_TYPES = ["Tomato", "Lettuce", "Carrot", "Cabbage", "Spinach"]
PRODUCERS = ["Farm001", "Farm002", "Farm003", "Farm004", "Farm005"]
PROCESSORS = ["Packing001", "Processing002", "Packing003"]
DISTRIBUTORS = ["Dist001", "Dist002", "Dist003"]
RETAILERS = ["Retail001", "Retail002", "Retail003"]
PERMISSIONS = ["Public", "Public", "Public", "Private", "Restricted"]


def batch_id(index: int) -> str:
    return f"BATCH{index:03d}"


def tx_id(index: int, hop: int) -> str:
    return f"TX{index:06d}_{hop}"


def ttl_header(target_triples: int, batch_count: int) -> str:
    return f"""# ProvChain-Org Benchmark Dataset: Supply Chain Traceability Scale Slice
# Format: RDF/Turtle (TTL)
# Generated by: benchmark-toolkit/scripts/generate-scale-dataset.py
# Target triples: {target_triples}
# Batch count: {batch_count}
# Purpose: scale-up confidence benchmark input; not primary paper evidence until campaign-validated

@prefix rdf: <http://www.w3.org/1999/02/22-rdf-syntax-ns#> .
@prefix rdfs: <http://www.w3.org/2000/01/rdf-schema#> .
@prefix prov: <http://www.w3.org/ns/prov#> .
@prefix xsd: <http://www.w3.org/2001/XMLSchema#> .
@prefix ex: <http://example.org/supplychain/> .
@prefix food: <http://example.org/food/> .
@prefix trace: <http://example.org/traceability#> .
@prefix perm: <http://example.org/permissions#> .

ex:Producer a rdfs:Class .
ex:Processor a rdfs:Class .
ex:Distributor a rdfs:Class .
ex:Retailer a rdfs:Class .
ex:Product a rdfs:Class .
ex:Transaction a rdfs:Class .

trace:hasProducer a rdf:Property .
trace:processedBy a rdf:Property .
trace:distributedBy a rdf:Property .
trace:soldBy a rdf:Property .
trace:hasTransaction a rdf:Property .
trace:nextTransaction a rdf:Property .
trace:transactionDate a rdf:Property .
trace:quantity a rdf:Property .
perm:hasPermission a rdf:Property .

perm:Permission a rdfs:Class .
perm:Public a perm:Permission .
perm:Private a perm:Permission .
perm:Restricted a perm:Permission .

"""


def actor_entities() -> str:
    lines: list[str] = ["# === Actors ===\n"]
    for i, producer in enumerate(PRODUCERS, start=1):
        lines.append(
            f'ex:Producer_{producer} a ex:Producer ; ex:name "Producer {i}" ; '
            f'ex:location "Region {i}" .\n'
        )
    for i, processor in enumerate(PROCESSORS, start=1):
        lines.append(
            f'ex:Processor_{processor} a ex:Processor ; ex:name "Processor {i}" ; '
            f'ex:license "PROC-{i:03d}" .\n'
        )
    for i, distributor in enumerate(DISTRIBUTORS, start=1):
        lines.append(
            f'ex:Distributor_{distributor} a ex:Distributor ; ex:name "Distributor {i}" ; '
            f'ex:license "DIST-{i:03d}" .\n'
        )
    for i, retailer in enumerate(RETAILERS, start=1):
        lines.append(
            f'ex:Retailer_{retailer} a ex:Retailer ; ex:name "Retailer {i}" ; '
            f'ex:type "Retail" .\n'
        )
    lines.append("\n")
    return "".join(lines)


def batch_block(index: int) -> str:
    product_type = PRODUCT_TYPES[(index - 1) % len(PRODUCT_TYPES)]
    producer = PRODUCERS[(index - 1) % len(PRODUCERS)]
    processor = PROCESSORS[(index - 1) % len(PROCESSORS)]
    distributor = DISTRIBUTORS[(index - 1) % len(DISTRIBUTORS)]
    retailer = RETAILERS[(index - 1) % len(RETAILERS)]
    permission = PERMISSIONS[(index - 1) % len(PERMISSIONS)]
    quantity = 50.0 + float((index * 7) % 175)
    production = datetime(2024, 1, 1, 6, 0, tzinfo=timezone.utc) + timedelta(hours=index)
    expiry = production + timedelta(days=14 + (index % 10))
    transaction = production + timedelta(days=1, hours=2)
    batch = batch_id(index)
    tx1 = tx_id(index, 1)
    tx2 = tx_id(index, 2)
    tx3 = tx_id(index, 3)

    return f"""ex:Product_{batch} a ex:Product ;
    ex:productType "{product_type}" ;
    ex:batchId "{batch}" ;
    trace:hasProducer ex:Producer_{producer} ;
    trace:processedBy ex:Processor_{processor} ;
    trace:distributedBy ex:Distributor_{distributor} ;
    trace:soldBy ex:Retailer_{retailer} ;
    trace:hasTransaction ex:Transaction_{tx1} ;
    ex:productionDate "{production.strftime('%Y-%m-%dT%H:%M:%SZ')}"^^xsd:dateTime ;
    ex:expiryDate "{expiry.strftime('%Y-%m-%dT%H:%M:%SZ')}"^^xsd:dateTime ;
    perm:hasPermission perm:{permission} .

ex:Transaction_{tx1} a ex:Transaction ;
    trace:transactionDate "{transaction.strftime('%Y-%m-%dT%H:%M:%SZ')}"^^xsd:dateTime ;
    trace:quantity {quantity:.1f} ;
    ex:from ex:Producer_{producer} ;
    ex:to ex:Processor_{processor} ;
    ex:product ex:Product_{batch} ;
    trace:nextTransaction ex:Transaction_{tx2} .

ex:Transaction_{tx2} a ex:Transaction ;
    trace:transactionDate "{(transaction + timedelta(hours=6)).strftime('%Y-%m-%dT%H:%M:%SZ')}"^^xsd:dateTime ;
    trace:quantity {quantity * 0.98:.1f} ;
    ex:from ex:Processor_{processor} ;
    ex:to ex:Distributor_{distributor} ;
    ex:product ex:Product_{batch} ;
    trace:nextTransaction ex:Transaction_{tx3} .

ex:Transaction_{tx3} a ex:Transaction ;
    trace:transactionDate "{(transaction + timedelta(hours=18)).strftime('%Y-%m-%dT%H:%M:%SZ')}"^^xsd:dateTime ;
    trace:quantity {quantity * 0.95:.1f} ;
    ex:from ex:Distributor_{distributor} ;
    ex:to ex:Retailer_{retailer} ;
    ex:product ex:Product_{batch} .

"""


def estimate_batches_for_target(target_triples: int) -> int:
    static_triples = 45
    triples_per_batch = 31
    return max(1, math.ceil((target_triples - static_triples) / triples_per_batch))


def generate_dataset(target_triples: int, batch_count: int | None) -> str:
    batches = batch_count or estimate_batches_for_target(target_triples)
    parts = [ttl_header(target_triples, batches), actor_entities(), "# === Batches ===\n\n"]
    parts.extend(batch_block(index) for index in range(1, batches + 1))
    parts.append(
        "# === Summary ===\n"
        f"# Logical batch count: {batches}\n"
        f"# Target triple class: supply_chain_{target_triples}\n"
        "# Evidence role: scale-up confidence input\n"
    )
    return "".join(parts)


def write_manifest(output: Path, content: str, target_triples: int, batch_count: int) -> None:
    manifest = {
        "dataset_file": output.name,
        "dataset_slice": output.stem,
        "generated_at_utc": datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ"),
        "generator": "benchmark-toolkit/scripts/generate-scale-dataset.py",
        "target_triples": target_triples,
        "logical_batch_count": batch_count,
        "sha256": hashlib.sha256(content.encode("utf-8")).hexdigest(),
        "bytes": len(content.encode("utf-8")),
        "evidence_role": "scale_up_confidence_not_primary_paper_evidence",
        "test_batch_ids": ["BATCH001", "BATCH010", "BATCH017", "BATCH025", "BATCH050"],
    }
    output.with_suffix(".manifest.json").write_text(
        json.dumps(manifest, indent=2) + "\n",
        encoding="utf-8",
    )


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--target-triples", type=int, required=True)
    parser.add_argument("--batches", type=int)
    parser.add_argument("--output", type=Path, required=True)
    args = parser.parse_args()

    content = generate_dataset(args.target_triples, args.batches)
    batch_count = args.batches or estimate_batches_for_target(args.target_triples)
    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(content, encoding="utf-8")
    write_manifest(args.output, content, args.target_triples, batch_count)
    print(f"wrote {args.output} ({batch_count} batches)")


if __name__ == "__main__":
    main()
